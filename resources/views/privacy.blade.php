    <p>Os Serviços são projetados para ajudar você a compartilhar informações com outros. Assim, algumas das informações geradas pelos Serviços são compartilhadas publicamente ou com terceiros.</p>

    <h2>Informações de endereço de IP</h2>

    <p>Podemos compartilhar informações limitadas de endereço de IP com nossos parceiros, prestadores de serviços e outras entidades com as quais fazemos negócios.</p>

    <h2>Informações agregadas</h2>

    <p>Compartilhamos informações agregadas com nossos parceiros, prestadores de serviços e outras pessoas com quem fazemos negócios. Compartilhamos esse tipo de dados estatísticos para que nossos parceiros possam entender como e quantas vezes as pessoas utilizam nossos Serviços e seus serviços ou sites, o que ajuda a melhorar tanto seus serviços quanto a forma como nossos Serviços fazem interface com eles. Além disso, esses terceiros podem compartilhar conosco informações sobre você que eles desenvolveram ou adquiriram de forma independente.</p>

    <h2>Comunicação por e-mail</h2>

    <p>Como parte dos Serviços, você poderá receber e-mails e outras comunicações que enviamos, como comunicações relativas à sua conta. Podemos utilizar seu endereço de e-mail para contatá-lo para fins editoriais, informá-lo a respeito de quaisquer alterações feitas em nosso Site e para lhe enviar mensagens sobre os produtos de nossos parceiros de marketing com seu consentimento ou conforme permitido por lei. Você pode optar por não receber nossos e-mails de marketing ao clicar no link de cancelamento nos e-mails. </p>

    <h2>Informação do perfil do usuário</h2>

    <p>Informações do perfil do usuário, incluindo seu nome de usuário e outras informações que você fornecer, podem ser exibidas para outros usuários para facilitar a interação entre usuários nos Serviços. Não revelaremos o seu endereço de e-mail para outros usuários. </p>

    <h2>Informações compartilhadas com nossos prestadores de serviços</h2>

    <p>Contratamos prestadores de serviços - pessoas e entidades que realizam determinadas tarefas em nosso nome. Podemos precisar compartilhar informações pessoais com nossos prestadores de serviços a fim de fornecer produtos ou serviços para você. Solicitamos que nossos prestadores de serviços utilizem as informações pessoais ou outras informações que compartilhamos com eles exclusivamente para a finalidade para nos ajudar.</p>

