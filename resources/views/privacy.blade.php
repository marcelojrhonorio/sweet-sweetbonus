    <p>Em alguns casos, podemos optar por adquirir ou vender ativos. Nesses tipos de transação, as informações do usuário, incluindo Informações Pessoais, normalmente são transferidas como um dos ativos comerciais. Além disso, as informações do usuário podem ser um dos ativos transferidos ou adquiridos por um terceiro. Você reconhece que tais transferências podem ocorrer conforme permitido por lei. Após a transferência de informações do usuário sob as circunstâncias descritas neste parágrafo, todos os pedidos relativos ao tratamento das informações do usuário devem ser direcionados à entidade à qual as informações são transferidas.</p>

