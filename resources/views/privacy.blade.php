    <h2>Cookies</h2>

    <p>O Sweetbonus ("nós", "nosso") utiliza cookies, sinalizadores de web e tecnologias similares (coletivamente, "cookies") em www.sweetbonus.com.br (o "Site") para funcionar de forma eficaz.</p>

    <h3>O que são cookies?</h3>

    <p>Cookies são pequenos textos que podem ser inseridos no seu computador por meio de seu navegador quando você acessa um site. Seu navegador armazena os cookies em associação a cada site que você visita. Utilizamos cookies para permitir aos nossos servidores reconhecer seu navegador e para nos informar como e quando você visita o Site, para compartilhar nosso conteúdo e para utilizar nossos serviços através da Internet.</p>

    <h3>Tipos de cookies</h3>

    <h4>Cookies essenciais</h4>

    <p>Utilizamos cookies essenciais enquanto cada sessão dura ("cookies de sessão"). Os cookies de sessão são essenciais para que você possa acessar o Site e suas funcionalidades com segurança. Os cookies de sessão são apagados quando você sai do site ou quando fecha o navegador de web.</p>

    <p>As informações a seguir podem ser recolhidas através de cookies de sessão: dados de acesso único e o ID da sessão; dados de afinidade e autenticação de servidor.</p>

    <h4>Cookies de publicidade</h4>
    Os cookies de terceiros listados são utilizados ​para fins de publicidade por web, como para entender o uso que você faz do Site e suas atividades on-line, bem como para lhe apresentar ofertas relevantes do Sweetbonus.com.br e anúncios adequados aos seus interesses, incluindo com base no país ou idioma que você selecionou.

    <h4>Cookies de desempenho</h4>

    <p>Cookies persistentes de terceiros são utilizados para fins de análise da web para nosso uso exclusivo, ou seja, para a gestão e melhoramento do desempenho e design do Site. Além disso, para aumentar a eficácia da entrega de anúncios, podemos entregar a você um sinalizador de web a partir de uma rede de anúncios por meio da utilização de nossos serviços. Sinalizadores de web permitem que as redes de anúncios enviem auditorias, pesquisas e relatórios anônimos e agregados para nós e para anunciantes.</p>

