    <h1>Política de Privacidade</h1>

    <p>Esta Política de Privacidade descreve as políticas e procedimentos do Sweet Bônus doravante chamado de (“Nós” ou “Nosso”) - com relação à recolha, utilização, compartilhamento e divulgação de suas informações em. Recebemos informações sobre você a partir de várias fontes, incluindo:</p>

    <ul>
        <li>os dados de inscrição que usuário realizou ou realizará no site Sweet Bônus. </li>
        <li>pela navegação do site de acordo com as resposta que o usuário deu.</li>
        <li>a partir de sites e serviços de terceiros, como DSP e DMPs. </li>
    </ul>

    <p>Quando você utiliza os Serviços, concorda com a coleta, transferência, manipulação, armazenamento, divulgação e outras utilizações de suas informações, conforme descrito nesta Política de Privacidade.</p>

    <h2>O que esta Política de Privacidade cobre?</h2>

    <p>Esta política de privacidade cobre:</p>

    <ul>
        <li>as formas de recolha de informação;</li>
        <li>o tratamento de informações pessoalmente identificáveis ​​("Dados Pessoais") e outras informações colhidas enquanto você utiliza ou acessa os Serviços.</li>
        <li>tratamento de qualquer Informação Pessoal que nossos parceiros de negócios compartilham conosco.</li>
        <li>utilização dos dados pessoais;</li>
        <li>as medidas de armazenamento e segurança adotadas no seu tratamento.</li>
    </ul>

    <p>Esta Política de Privacidade não se aplica às práticas de terceiros que não nos pertencem ou controlamos, por isso encorajamos você a analisar cuidadosamente as políticas de privacidade de quaisquer Serviços de Terceiros que acessar ou contratar.</p>

    <h2>Porque e quais informações coletamos?</h2>

    <p>As informações que coletamos nos permitem personalizar, melhorar e continuar a operar os Serviços. Coletamos os seguintes tipos de informações de nossos usuário: nome; endereço; emails; números de telefone; endereço IP; dados demográficos e sobre estilos de vida - essa coleta poderá ser realizada através de formulários online ou por compra de informação de terceiros.</p>

    <p>Conforme permitido por lei, podemos utilizar suas informações de contato para lhe enviar informações sobre nossos serviços quando acreditarmos que tais informações sejam importantes. Você poderá cancelar o recebimento destas mensagens através de suas configurações de Conta.</p>

    <h2>Informações recolhidas automaticamente</h2>

    <p>Recebemos e registramos automaticamente as informações de seu dispositivo ou navegador web quando você interage com o site SweetBônus. Esta informação pode incluir seu endereço de IP, um ID que poderemos criar e associar a nosso conteúdo, informações de cookies e outros detalhes. Esta informação é utilizada para combater spam/malware, para facilitar a recolha de dados relativos à sua interação com os serviços e para personalizar, melhorar e continuar a operar os Serviços.</p>

    <p>De modo geral, os Serviços recolhem automaticamente informações de uso, como o número e a frequência de visitantes. Podemos utilizar esses dados de forma agregada, ou seja, como medida estatística ou de forma individual. Este tipo de dados agregados capacita a nós e a terceiros por nós autorizados a descobrir com que frequência as pessoas utilizam partes dos Serviços para que, assim, possamos analisar e melhorá-los.</p>

    <p>Quando você visitar o Site, também podemos coletar informações sobre suas atividades on-line ao longo do tempo e em sites de terceiros ou serviços on-line. </p>

    <h2>Dados de Localização</h2>

    <p>Alguns dos nossos serviços podem coletar dados de localização geográfica (que determinam sua localização atual) para fornecer funcionalidades de Serviço relacionadas à localização. Podemos vincular dados de localização a outras informações que coletamos sobre você para os fins descritos nesta Política de Privacidade.</p>

    <h2>Comunicação por e-mail</h2>

    <p>Podemos receber uma confirmação quando você abrir um e-mail enviado por nós. Utilizamos essa confirmação para melhorar nossos serviços, incluindo o atendimento ao cliente.</p>

    <p>Algumas das comunicações de e-mail são realizadas por empresas que nos patrocinam, a quem os dados do usuário são transmitidos. O usuário dará sempre o seu consentimento expresso e individual de partilhar os seus dados com cada patrocinador. Poderá no devido momento do processo de inscrição escolher os patrocinadores com quem concorda partilhar ou não partilhar os seus dados.</p>

    <p>Todos os participantes cadastrados também compartilham os dados com a Caloga, patrocinadora do Sweet Media Lda. situada em 170 rue Raymond Losserand, 75014 Paris, França. A política de privacidade da Caloga está disponível em: <a target="_blank" href="http://caloga.br.com/html/dperso.php">http://caloga.br.com/html/dperso.php</a>.</p>

